def str_stem(s): 
    if isinstance(s, str):
        s = re.sub(r"(\w)\.([A-Z])", r"\1 \2", s) #Split words with a.A
        #s = re.sub(r'( [a-z]+)([A-Z][a-z])', r'\1 \2', s)
